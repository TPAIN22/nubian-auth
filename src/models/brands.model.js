import mongoose from 'mongoose';

const merchantchema = new mongoose.Schema({
    name: {
        type: String,
        required: true,
        trim: true,
    },
    description: {
        type: String,
        required: true,
        trim: true,
    },
    image: {
        type: String,
        required: true,
    },
    isActive: {
        type: Boolean,
        default: true,
    },
    createdAt: {
        type: Date,
        default: Date.now,
    },
    updatedAt: {
        type: Date,
        default: Date.now,
    },
});

merchantchema.pre('save', function(next) {
    this.updatedAt = Date.now();
    next();
});

const Brand = mongoose.model('Brand', merchantchema);
export default Brand;

